function findMergeNode(headA, headB) {
    let a = headA
    let b = headB
    while(a.next){
        let temp = a
        a = a.next
        temp.next = null
    }
    while(b){
        b = b.next
        if(b.next === null){
            return b.data
        }
    }
    return null
}